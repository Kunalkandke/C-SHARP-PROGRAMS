using System;

class Program
{
    static void Main()
    {
        int a = -10;

        Console.WriteLine("Absolute = " + Math.Abs(a));
        Console.WriteLine("Square Root of 25 = " + Math.Sqrt(25));
        Console.WriteLine("Power = " + Math.Pow(2, 3));
        Console.WriteLine("Maximum = " + Math.Max(10, 20));
    }
}
